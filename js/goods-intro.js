//热销分类
$('#category1').mouseover(function(){
	$('#arrow-right1').css('display','none');
	$('#goods-intro1').css('display','flex').css('z-index','1000');
	$('.bg-goods-category').css('border-right','1px solid #fff');
	$('.icon1').css('display','inline-block');
});
$('#category1').mouseout(function(){
	$('#arrow-right1').css('display','inline-block');
	$('#goods-intro1').css('display','none');
	$('.bg-goods-category').css('border-right','1px solid #D51819');
	$('.icon1').css('display','none');
});

